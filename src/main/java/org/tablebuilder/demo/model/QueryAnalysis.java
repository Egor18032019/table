package org.tablebuilder.demo.model;

import lombok.Data;

import java.util.List;

/**
 * Класс для хранения результатов анализа запроса.
 * Содержит ключевые слова и метаданные для последующего поиска.
 */
@Data
public class QueryAnalysis {
    /**
     * Список ключевых слов, извлеченных из запроса пользователя.
     * Используется для поиска по названиям файлов, листов и колонок.
     */
    private List<String> searchKeywords;

    /**
     * Намерение пользователя (зарезервировано для будущего использования).
     * Может использоваться для уточнения стратегии поиска.
     */
    private String intent = "поиск_файлов";
}